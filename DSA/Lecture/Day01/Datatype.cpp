#include<iostream>
using namespace std;

// Data type in c++
int main() {
    // Integer data type
    int a = 10;
    cout << "Integer: " << a << endl;
    // Character data type
    char b = 'A';
    cout << "Character: " << b << endl;
    // Float data type
    float c = 5.75;
    cout << "Float: " << c << endl;
    // Double data type
    double d = 19.99;
    cout << "Double: " << d << endl;
    // Boolean data type
    bool e = true;
    cout << "Boolean: " << e << endl;
    // String data type
    string f = "Hello, World!";
    cout << "String: " << f << endl;
    // Void data type (not directly printable)
    void* g = nullptr; // Using a void pointer
    cout << "Void pointer: " << g << endl;
    return 0;
}