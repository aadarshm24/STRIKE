#include<iostream>
using namespace std;

int main() {
    cout << "Hello, World!" << endl;
    // data types
    int a = 10;          // integer
    float b = 5.5;      // floating point
    double c = 9.99;    // double precision floating point
    char d = 'A';       // character
    bool e = true;      // boolean
    cout << "Integer: " << a << endl;
    cout << "Float: " << b << endl;
    cout << "Double: " << c << endl;
    cout << "Character: " << d << endl;
    cout << "Boolean: " << e << endl;
return 0;
}